#integer
age = 25
print(age)
print(type(age))
print("age: ", age)
print("age is of type:", type(age))

#float
height = 5.0
print(height)
print(type(height))
print("height:", height)
print("height is of the type:", type(height))

#string
name = "harry"
print(name)
print(type(name))
print("name:", name)
print("name is of the:", type(name))

#boolean
rajesh = True
print(rajesh)
print(type(rajesh))

#list
marks = [20, 40, 60]
print(marks)
print(type(marks))
print("type:", marks)
print("type of marks is:", marks)
#tuple
dimension = (15, 25,35)
print(dimension)
print(type(dimension))
print("dimension:",dimension)
print("type of dimension is:", dimension)

